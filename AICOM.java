import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef {
    public static void main (String[] args) throws java.lang.Exception {
        Scanner mouni = new Scanner(System.in);
        int siddhu = mouni.nextInt();
        
        if (siddhu <= 60) {
            System.out.println("YES");
        } else {
            System.out.println("NO");
        }
        
        mouni.close();
    }
}
